
public class Person {
	public String name = null;
	public int age = 0;

	public Person(){}						//①
	public Person(String name, int age){	//②
		this.name = name;
		this.age = age;
	}
	public Person(String name){				//③
		this.name = name;
		this.age = 0;
	}
	public Person(int age){					//④
		this.name = "名前なし";
		this.age = age;
	}

}
